Для действий "Восстановить" и "Удалить" вы должны выбрать файл.
<UL>
	<li><strong>Восстановить:</strong> база данных будет восстановлена из записей, которые находятся в выбранном файле.</li>
	<li><strong>Удалить:</strong> вы можете удалить выбранные файлы резервных копий.</li>
	<li><strong>Новая резервная копия:</strong> здесь вы можете начать создание новой резервной копии с параметрами, указанными в конфигурации.</li>
</UL>

